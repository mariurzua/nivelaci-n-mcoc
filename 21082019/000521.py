# Se aprende en este codigo a que un elemento de la lista puede ser reemplazado por otro. Esto se logra
# mediante el llamado de la lista y entre los corchetes la posicion del elemento que se desea reemplazar
# y se iguala a el nuevo elemento. 

a = [3, 10, -1, 1]     #creacion lista "a" con 4 elementos, posiciones 0, 1, 2, 3.

a[0]= 100              #reemplazo elemento en ubicacion 0 de la lista (un 3), por el numero 100.     
print (a)              #impresion nueva lista con elemento reemplazado.
