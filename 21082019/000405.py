# Se aprende a imprimir un elemento dentro de la lista. Esto se logra mediante el uso de la posicion, ya
# que se posicionan segun el orden en que van, partiendo con la posicion 0, 1, 2... y asi.

a = [3, 10, -1, 1]     #creacion lista "a" con 4 elementos, posiciones 0, 1, 2, 3.

print (a[0])           #impresion primer elemento de la lista, correspondiente al ubicado en posicion 0.   
print (a[1])           #impresion segundo elemento de la lista, correspondiente al ubicado en posicion 1. 
print (a[3])           #impresion cuarto elemento de la lista, correspondiente al ubicado en posicion 3. 
